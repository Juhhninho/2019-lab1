
public class EstruturaRepeticaoPara {

	public static void main(String[] args) {
		// utilizado quando preciso executar um loop de repetição determinado numero de vezes!
		for (int i = 0; i < 5; i = i + 1) {
			System.out.println("Olá Mundo!!!");
			System.out.println(i);
		}
		
		System.out.println("%%%%%%%%%%%%%%%%");
		for (int i = 0; i < 5; i++) {
			System.out.println(i);
		}
		
		System.out.println("%%%%%%%%%%%%%%%%");
		for (int i = 5; i > 0; i--) {
			System.out.println(i);
		}
		
	}
	
}
